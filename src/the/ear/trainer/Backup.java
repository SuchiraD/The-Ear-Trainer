    private void resoreLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_resoreLabelMouseClicked




        String path=directoryTextField.getText()+"/"+restoreNameTextField.getText();
      
        Database backupDatabase=new Database();
        try {
            boolean result = backupDatabase.restoreBackup("ear_trainer", "root", "", path);
            if(result){
                JOptionPane.showMessageDialog(this, "Backup Restore successfully", "Message", JOptionPane.PLAIN_MESSAGE,new ImageIcon("./src/icons/success.png"));
            }
        } catch (Exception ex) {
            Logger.getLogger(Backup.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Could not restore the backup", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_resoreLabelMouseClicked

