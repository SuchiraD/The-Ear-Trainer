    private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showSaveDialog(this);
        if(result==JFileChooser.APPROVE_OPTION) {
            fileNameTextField.setText(fileChooser.getSelectedFile().getName()+".sql");
            saveInTextField.setText(fileChooser.getCurrentDirectory().toString());

        }

    }//GEN-LAST:event_browseButtonActionPerformed

    private void backupLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_backupLabelMouseClicked
        Database backupDatabase=new Database();
        String path=saveInTextField.getText()+"/"+fileNameTextField.getText();
        boolean bool;
        try {
            bool = backupDatabase.getBackup("ear_trainer", "root", "", path);
            if(bool){
                JOptionPane.showMessageDialog(this, "Backup created successfully", "Message", JOptionPane.PLAIN_MESSAGE,new ImageIcon("./src/icons/success.png"));
            }else{
                JOptionPane.showMessageDialog(this, "Could not create the backup", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(Backup.class.getName()).log(Level.SEVERE, null, ex);

        }

    }//GEN-LAST:event_backupLabelMouseClicked

