    private void setAnswers(){
        answersComboBox.removeAllItems();
        if(answers != null){
            answersComboBox.setMaximumRowCount(answers.size());
            for (String answer : answers) {
                answersComboBox.addItem(answer);
            }
        }
        else{
            answersComboBox.removeAllItems();
        }
    }
    
